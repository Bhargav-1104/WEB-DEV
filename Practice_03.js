// 1. for loop
num = 10;
for (i = 0; i < num; i++) {
  console.log(i);
}

// 2. for-in loop
const object = {
  name: "bhargav",
  surname: "sharma",
  age: 21,
};

for (i in object) {
  console.log(i);
}

// 3. for-of loop

for (values of i) {
  console.log(values);
}

